                     <h3>Difference between Mutual Fund and Portfolio Management</h3>
                     <p style="text-align: justify;"><span style="font-size: small; font-family: verdana, geneva;">Making money off the market requires certain degree of expertise and a healthy appetite for risk. While most people start off early and eventually go on to learn how the market behaves and thereby invest their monies, there are investors who enter the scenario with large amounts of monies and seek help from professionals for the purpose of earning above-average returns from their savings. Towards this end, some opt for portfolio management services (PMS) offered by various entities registered with the SEBI.</span><br /><span style="font-size: small; font-family: verdana, geneva;">Portfolio management services have equity and debt options. As far as mutual funds are concerned, they are more tax-friendly. Now let 's see how the two competitors, portfolio management services and equity mutual funds, differ from each other, and how they fare against each other.</span></p>
                     <p style="text-align: justify;"><span style="font-size: small; font-family: verdana, geneva;"><strong>Customised investment:</strong> It's commonly believed that portfolio management services offer customised services, with many brokerages offering you the choice of different model portfolios such as large-cap and mid-cap depending on your needs. However, this is not a scalable model and large portfolio managers won 't do it for just 50 lakhs. They usually do it for investors who fall under HNI category. However, you can get certain specific customisation done with 50 lakhs as well. Mutual funds offer no such options.</span></p>
                     <p style="text-align: justify;"><span style="font-size: small; font-family: verdana, geneva;"><strong>Transparency:</strong> Portfolio management services offer complete transparency in money management. You will be aware of every purchase and sale of shares, brokerage, date of transaction, portfolio manager 's exact fee amongst others. This level of transparency also lets you know where your portfolio manager made you money and where he lost it. In mutual funds, you get a monthly report of final holdings and the quarterly total expense ratio. Having said this, it is very difficult to gauge the performance of a portfolio management service product before investing in it. There is nothing called a scheme performance since it comprises of individual portfolios created as per individual needs.</span></p>
                     <p style="text-align: justify;"><span style="font-size: small; font-family: verdana, geneva;"><strong>Flexibility:</strong> Unlike mutual funds, a portfolio management service is not restricted to a stated objective and stringent set of terms. This offers flexibility to the portfolio manager in terms of how and when he wants to invest/pull back your money. If he senses risk, he can take aggressive cash calls. If the situation demands, he can even sell off all the equity holdings and maintain 100 % cash position. This is great for protection against market crashes like the one in 2008. However, it 's rare that portfolio managers take such bold cash calls.</span></p>
                     <p style="text-align: justify;"><span style="font-size: small; font-family: verdana, geneva;"><strong>Separate status:</strong> In portfolio management services, your portfolio is treated as a separate entity. This prevents others ' action affecting your portfolio. Let 's say, if a lot of investors are redeeming simultaneously, then the fund manager of a mutual fund may have to create liquidity by selling off the most liquid stocks. This changes the fund 's portfolio and can have adverse affects on the portfolio of the people who stay invested. Whereas in portfolio management services, the portfolio manager will sell the entire portfolios of specific clients only, and not affect your portfolio if you wish to stay invested.</span></p>
                     <p style="text-align: justify;"><span style="font-size: small; font-family: verdana, geneva;"><strong>Fee structure:</strong> Mutual funds have a fixed fee structure. Portfolio management services in india providers will offer you more than one option with the same fee structure.</span></p>
                     <p style="text-align: justify;"><span style="font-size: small; font-family: verdana, geneva;"><strong>Size vs cost:</strong> Mutual funds investments even accept SIPs of INR 500, while portfolio management services has a mandatory of minimum 50 lakhs.</span></p>
                     <p style="text-align: justify;"><span style="font-size: small; font-family: verdana, geneva;"><strong>Taxation:</strong> Mutual funds offer schemes and have a pass through status, which means a fund manager may buy and sell stocks any number of times without incurring tax. Whereas in the case of portfolio management services, it 's you holding stocks in your name, so every time the portfolio manager sells a share, there is capital gain or loss.</span></p>
                     <p style="text-align: justify;"><span style="font-size: small; font-family: verdana, geneva;"><strong>Accountability:</strong> Portfolio management service managers are directly answerable to you, whereas mutual fund managers have no such obligations.</span></p>
                     <p style="text-align: justify;"><span style="font-size: small; font-family: verdana, geneva;">So now that you know how mutual funds and portfolio management services differ from each other, spend good amounts of time in understanding the market, seek professional help in this regard, and take a call. Whatever be your choice, remember that you will not only have to invest monies, but also time if you really want to reap monetary benefits from market movements.</span></p>
