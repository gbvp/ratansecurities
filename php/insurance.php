                     <h3>Life Insurance</h3>
                     <p style="text-align: justify;"><span style="font-size: small; font-family: verdana, geneva;">Life insurance is nothing but, "Insured your life". L"IF"E is full of IF's... and one needs to plan to secure their loved ones and themselves against the Ifs of Life. When you opt for a life insurance plan, you transfer your family's financial risks in case of any unfortunate event to the life insurer. This allows you and your loved ones to live life fearlessly. Life Insurance protects your family's financial wellbeing from the consequences of living without an income.</span></p>
                     <p style="text-align: justify;"><strong><span style="font-size: small; font-family: verdana, geneva;">TYPES OF LIFE INSURANCE</span></strong></p>
                     <ol>
                        <li style="text-align: justify;"><span style="font-size: small; font-family: verdana, geneva;"><strong>TERM LIFE INSURANCE :</strong> Term Life Insurance plans are designed for specified periods of years,say 10,15 or 20 years etc.These are the &ldquo;cheapest plans&rdquo; compared to other plans.Main disadvantage is,there is &ldquo;no any survival benefit&rdquo;.The policy holder gets the claim amount only when death is happens.</span></li>
                        <li style="text-align: justify;"><span style="font-size: small; font-family: verdana, geneva;"><strong>UNIT LINKED INSURANCE PLAN ( ULIP&rsquo;S ) :</strong> Unit Linked Insurance plans(ULIPs) offers policyholder &ldquo;life security plus investment opportunity&rdquo;. Premium paid into this policy is bifurcated into two parts, &ldquo;one for the purpose of Life insurance and another for the purpose of investment&rdquo;. These policies are &lsquo;linked&rsquo; to market products like mutual funds, bonds, stocks, etc.</span></li>
                        <li style="text-align: justify;"><span style="font-size: small; font-family: verdana, geneva;"><strong>ENDOWMENT PLAN :</strong> Endowment Plan offers&rdquo;life security plus survival benefit&rdquo;.The main advantage of this plan is &ldquo;policy holder gets assured &amp; lumpsum amount at maturity&rdquo;.</span></li>
                        <li style="text-align: justify;"><span style="font-size: small; font-family: verdana, geneva;"><strong>WHOLE LIFE POLICY :</strong> Whole life policy offers protection for the &ldquo;entire lifetime&rdquo; of an individual. Certain insurers can have an upper age limit for maturity of policy. wherein a &rdquo;death benefit is provided to the nominee on demise of the policyholder&rdquo;. If there is a maturity benefit associated with the plan, a maturity amount will be paid when the policyholder attains the upper age limit associated with the scheme.</span></li>
                        <li style="text-align: justify;"><span style="font-size: small; font-family: verdana, geneva;"><strong>ANNUITY / PENSION POLICY :</strong> annuity/pension plans can be used by individuals looking to &ldquo;financially secure their retired life&rdquo;.The amount collected in the form of premium and distributed.</span></li>
                        <li style="text-align: justify;"><span style="font-size: small; font-family: verdana, geneva;"><strong>MONEY BACK POLICY :</strong> to the policy holder.Insurer pays certain percentage of sum insured amount to the policy holder at regular intervals.For example,let&rsquo;s say 20 years policy,the insurer pays 20% of sum assured amount after every 5 years and remaining 40% at maturity period.</span></li>
                        <li style="text-align: justify;"><span style="font-size: small; font-family: verdana, geneva;"><strong>CHILD INSURANCE POLICY :</strong> A child insurance policy is a combination of saving &amp; investment plan,to fulfill their future financial dreams/goals. A child insurance policy allows you do invests since child&rsquo;s born age to adult age.Some policies allows you to withdraw your savings at certain intervals.</span></li>
                     </ol>
